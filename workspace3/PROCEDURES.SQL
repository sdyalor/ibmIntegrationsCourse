PROCEDURE GET_CODE_BY_ID(MESSAGE_ID IN NUMBER, C_PARAMETER OUT SYS_REFCURSOR) IS BEGIN
  OPEN C_PARAMETER FOR 
                    SELECT ID,CODE,STATUS FROM FORMAT_MESSAGE
                    WHERE ID = MESSAGE_ID;
END GET_CODE_BY_ID;