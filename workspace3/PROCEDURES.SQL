PROCEDURE SP_READ_PROD (P_COD_PROD IN INTEGER, C_PARAMETER OUT SYS_REFCURSOR);
PROCEDURE SP_INSERT_PROD (P_COD_PRODUCTO IN VARCHAR2 ,P_NOM_PRODUCTO IN VARCHAR2 ,P_CANTIDAD IN NUMBER, P_PRECIO_UNIT IN NUMBER); 
PROCEDURE SP_UPDATE_PROD(ICOD_PRODUCTO IN VARCHAR,INOM_PRODUCTO IN VARCHAR,ICANTIDAD IN NUMBER,IPRECIO IN NUMBER);
PROCEDURE SP_DELETE_PROD (P_COD_PRODUCTO IN VARCHAR);


PROCEDURE SP_READ_PROD (P_COD_PROD IN INTEGER, C_PARAMETER OUT SYS_REFCURSOR) AS BEGIN
  OPEN C_PARAMETER FOR
    SELECT  COD_PRODUCTO, NOM_PRODUCTO, CANTIDAD, PRECIO_UNIT 
    FROM PRODUCTO
    WHERE COD_PRODUCTO = P_COD_PROD;
END SP_READ_PROD;

PROCEDURE SP_UPDATE_PROD(ICOD_PRODUCTO IN VARCHAR,INOM_PRODUCTO IN VARCHAR,ICANTIDAD IN NUMBER,IPRECIO IN NUMBER) AS BEGIN
UPDATE PRODUCTO SET CANTIDAD = ICANTIDAD , NOM_PRODUCTO = INOM_PRODUCTO, PRECIO_UNIT = IPRECIO where COD_PRODUCTO = ICOD_PRODUCTO;
END SP_UPDATE_PROD;



PROCEDURE SP_DELETE_PROD (P_COD_PRODUCTO IN VARCHAR) AS BEGIN
    DELETE FROM PRODUCTO
    WHERE COD_PRODUCTO = P_COD_PRODUCTO;
END SP_DELETE_PROD;

PROCEDURE SP_INSERT_PROD (P_COD_PRODUCTO IN VARCHAR2 ,P_NOM_PRODUCTO IN VARCHAR2 ,P_CANTIDAD IN NUMBER, P_PRECIO_UNIT IN NUMBER) IS BEGIN 
INSERT INTO PRODUCTO   (COD_PRODUCTO,
                            NOM_PRODUCTO,
                            CANTIDAD,
                            PRECIO_UNIT)
    VALUES (P_COD_PRODUCTO, 
                P_NOM_PRODUCTO,
                P_CANTIDAD,
                P_PRECIO_UNIT);
END SP_INSERT_PROD;
